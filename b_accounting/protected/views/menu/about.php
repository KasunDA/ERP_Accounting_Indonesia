<p>Kingdom Application Framework - Powered by Yii Framework</p>

<p>
	Kingdom Application Framework dibangun di atas <a
		href="http://www.yiiframework.com">Yii Framework</a>, sebuah Web
	Framework yang menggunakan bahasa pemrograman PHP. Memiliki
	karakteristik Open Source, dapat dijalankan di semua Operating System,
	baik Windows, Linux, Mac, dsb dan merupakan Framework PHP terbaik saat
	ini yang mengedepankan Kecepatan, Keamanan dan Profesional (Fast,
	Secure, Professional).
</p>

<p>Kingdom Application Framework mengadopsi pemahaman yang sama, yakni
	Open Source sehingga code programming yang dibuat dalam aplikasi ini
	dapat dibuka secara transparan dan dapat dimodifikasi sesuai dengan
	kebutuhan customer. Dengan demikian segala resiko yang ditimbulkan
	akibat perubahan code tanpa sepengetahuan kami, menjadi tanggung jawab
	end user.</p>

<p>Demi tegaknya Keadilan dan Hak Atas Kekayaan Intelektual, segala
	bentuk penggunaan dan penggandaan aplikasi diluar End User yang
	terdaftar dalam database kami, wajib memberi tahu kepada kami dan sudah
	seharusnya mendapatkan kompensasi yang sewajarnya.</p>

<p>Definisi Kingdom Application Framework diambil dari prinsip-prinsip
	penegakkan Kingdom of Heaven (Kerajaan Allah) di muka bumi. Orang-orang
	yang terlibat dalam ministry maupun market place, bergandengan tangan
	untuk menegakkan Kerajaan Allah di muka bumi melalui dunia Teknologi
	Informasi, Komunikasi dan Media.</p>

<p>Kiranya Tuhan memberkati segala pelayanan dan pekerjaan kita. Tuhan
